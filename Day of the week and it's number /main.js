//EXERCISE: Given a variable "y" (that can contain a number from 1 to 7), determine which day of the week it corresponds to. 
//Include the option if the variable takes an invalid value (for example 0 or 8). //

let y = 1;

if (y == 1) {
    console.log('Monday')
} else if (y == 2) {
    console.log('Tuesday')
} else if (y == 3) {
    console.log('Wednesday')
} else if (y == 4) {
    console.log('Thursday')
} else if (y == 5) {
    console.log('Friday')
} else if (y == 6) {
    console.log('Saturday')
} else if (y == 7) {
    console.log('Sunday')
} else {
    console.log('Invalid value')
}
